package br.com.dio.loops;
/*
    Exiba um texto 5 vezes, com uma expressao de controle e uma variavel booleana
 */
public class Ex8_Texto {
    public static void main(String[] args) {
        int controler = 0;
        while (controler < 5) {
            System.out.println("Texto");
            controler++;
        }
    }
}
